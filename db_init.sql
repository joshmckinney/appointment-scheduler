-- CREATE customerview to extract data for objects
CREATE VIEW customerview AS SELECT customerId,active,customerName,address,phone
FROM customer,address
WHERE address.addressId = customer.customerId;

-- Test the view
SELECT * FROM customerview;

-- Create appointmentview for need appt cols
CREATE VIEW appointmentview AS SELECT
appointmentId AS apptId,u.userId AS consId, c.customerId AS custId,u.userName AS userName,
customerName AS custName,type AS apptType,start AS apptStart,end AS apptEnd
FROM appointment left join customerview c on appointment.customerId = c.customerId
JOIN user u on appointment.userId = u.userId;

